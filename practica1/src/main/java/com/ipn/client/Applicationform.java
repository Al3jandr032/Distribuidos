    public Alumno getAlumno() {
        String nombre = this.nombreTextField.getText();
        String paterno = this.apaternoTextField.getText();
        String materno = this.amaternoTextField.getText();
        String email = this.emailTextField.getText();
        String calle = this.calleTextField.getText();
        String numero = this.numeroTextField.getText();
        String colonia = this.coloniaTextField.getText();
        String delegacion = this.delegacionTextField.getText();
        String entidad = this.entidadTextField.getText();
        String noTelefono = this.telefonoTextField.getText();
        alumno = new Alumno(nombre, paterno, materno,  email, calle, numero, 
                colonia, delegacion,  entidad, noTelefono);
        return alumno;
    }

