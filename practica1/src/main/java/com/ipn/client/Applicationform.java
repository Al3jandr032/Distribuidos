    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        Alumno aux = this.getAlumno();
        
        if(this.mode == Applicationform.CREATE){
            this.operationCreate(aux);
        }else{
            aux.setIdAlumno(id_alumno);
            this.operationUpdate(aux);
        }
        this.dispose();
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private boolean operationCreate(Alumno a){
        boolean succesful = true;
            System.out.println("creando Alumno: ");
           
            try (Socket socket = new Socket("127.0.0.1", Server.PORT)) {
                ObjectOutputStream dos = new ObjectOutputStream(socket.getOutputStream());
                ObjectInputStream is = new ObjectInputStream(socket.getInputStream());
                
                dos.writeInt(Server.ALUMNO);
                dos.writeInt(Server.CREATE);
                dos.writeObject(a);
                dos.flush();
                dos.close();
                
            } catch (IOException ex) {
                System.out.println(ex.getMessage());
                System.out.println(ex.getClass());
                System.out.println(ex.getCause());
                succesful = false;
            } 
        
        return succesful;
    }
    
